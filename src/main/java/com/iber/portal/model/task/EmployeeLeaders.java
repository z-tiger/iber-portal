package com.iber.portal.model.task;

public class EmployeeLeaders {
	private String type;
	private String identifyLabel;
	private String employeeLeaders;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getIdentifyLabel() {
		return identifyLabel;
	}

	public void setIdentifyLabel(String identifyLabel) {
		this.identifyLabel = identifyLabel;
	}

	public String getEmployeeLeader() {
		return employeeLeaders;
	}

	public void setEmployeeLeader(String employeeLeaders) {
		this.employeeLeaders = employeeLeaders;
	}

}
