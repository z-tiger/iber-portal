package com.iber.portal.vo.car;

public class CarPosition {

	private String position ;

	
	public CarPosition() {
		super();
	}

	public CarPosition(String position) {
		super();
		this.position = position;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}
	
	
}
