public class carRunAnalysisVo {
	private long rentCarNumber;
	private long orderTotal;
	private long timeTotal;
	private long moneyTotal;
	private long totalCarSum;
	
	public long getRentCarNumber() {
		return rentCarNumber;
	}
	public void setRentCarNumber(long rentCarNumber) {
		this.rentCarNumber = rentCarNumber;
	}
	public long getOrderTotal() {
		return orderTotal;
	}
	public void setOrderTotal(long orderTotal) {
		this.orderTotal = orderTotal;
	}
	public long getTimeTotal() {
		return timeTotal;
	}
	public void setTimeTotal(long timeTotal) {
		this.timeTotal = timeTotal;
	}
	public long getMoneyTotal() {
		return moneyTotal;
	}
	public void setMoneyTotal(long moneyTotal) {
		this.moneyTotal = moneyTotal;
	}
	public long getTotalCarSum() {
		return totalCarSum;
	}
	public void setTotalCarSum(long totalCarSum) {
		this.totalCarSum = totalCarSum;
	}
}
