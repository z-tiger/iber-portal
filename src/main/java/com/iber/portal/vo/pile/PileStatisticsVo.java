package com.iber.portal.vo.pile;

public class PileStatisticsVo {
	
	
	private Integer pileNums = 0 ;
	private String name;
	private String code;
	private String type;

	public Integer getPileNums() {
		return pileNums;
	}

	public void setPileNums(Integer pileNums) {
		this.pileNums = pileNums;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
